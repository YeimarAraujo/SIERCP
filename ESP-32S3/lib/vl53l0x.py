import ustruct
import utime

# Timeout en ms para operaciones I2C
_IO_TIMEOUT = 5000

# ------------------------------------------------------------
# DRIVER VL53L0X para MicroPython
# Registros del sensor (valores literales, sin const())
# BUGS CORREGIDOS:
#   1. const() eliminado (no disponible en modulos /lib/)
#   2. vhv_byte no definido -> reemplazado por vhv en _calibrar()
#   3. Doble definicion de _IO_TIMEOUT eliminada
#   4. Metodo read() agregado como alias de ping()
# ------------------------------------------------------------
class VL53L0X:
    def __init__(self, i2c, address=0x29):
        self.i2c       = i2c
        self.address   = address
        self._stop_var = 0
        self._init()

    def _r8(self, reg):
        return self.i2c.readfrom_mem(self.address, reg, 1)[0]

    def _w8(self, reg, val):
        self.i2c.writeto_mem(self.address, reg, bytes([val]))

    def _r16(self, reg):
        d = self.i2c.readfrom_mem(self.address, reg, 2)
        return ustruct.unpack('>H', d)[0]

    def _init(self):
        self._w8(0x89, self._r8(0x89) | 0x01)
        self._w8(0x88, 0x00)
        self._w8(0x80, 0x01)
        self._w8(0xFF, 0x01)
        self._w8(0x00, 0x00)
        self._stop_var = self._r8(0x91)
        self._w8(0x00, 0x01)
        self._w8(0xFF, 0x00)
        self._w8(0x80, 0x00)
        self._w8(0x60, self._r8(0x60) | 0x12)
        self._w8(0x44, 0)
        self._w8(0x45, 32)
        self._w8(0x01, 0xFF)
        spad_count, aperture = self._spad_info()
        refs = self.i2c.readfrom_mem(self.address, 0xB0, 6)
        self._w8(0xFF, 0x01)
        self._w8(0x4F, 0x00)
        self._w8(0x4E, 0x2C)
        self._w8(0xFF, 0x00)
        self._w8(0xB6, 0xB4)
        first   = 12 if aperture else 0
        enabled = 0
        data    = bytearray(6)
        for i in range(48):
            if i < first or enabled == spad_count:
                data[i // 8] &= ~(1 << (i % 8))
            elif (refs[i // 8] >> (i % 8)) & 1:
                data[i // 8] |= (1 << (i % 8))
                enabled += 1
        self.i2c.writeto_mem(self.address, 0xB0, bytes(data))
        for r, v in [
            (0xFF,0x01),(0x00,0x00),(0xFF,0x00),(0x09,0x00),(0x10,0x00),
            (0x11,0x00),(0x24,0x01),(0x25,0xFF),(0x75,0x00),(0xFF,0x01),
            (0x4E,0x2C),(0x48,0x00),(0x30,0x20),(0xFF,0x00),(0x30,0x09),
            (0x54,0x00),(0x31,0x04),(0x32,0x03),(0x40,0x83),(0x46,0x25),
            (0x60,0x00),(0x27,0x00),(0x50,0x06),(0x51,0x00),(0x52,0x96),
            (0x56,0x08),(0x57,0x30),(0x61,0x00),(0x62,0x00),(0x64,0x00),
            (0x65,0x00),(0x66,0xA0),(0xFF,0x01),(0x22,0x32),(0x47,0x14),
            (0x49,0xFF),(0x4A,0x00),(0xFF,0x00),(0x7A,0x0A),(0x7B,0x00),
            (0x78,0x21),(0xFF,0x01),(0x23,0x34),(0x42,0x00),(0x44,0xFF),
            (0x45,0x26),(0x46,0x05),(0x40,0x40),(0x0E,0x06),(0x20,0x1A),
            (0x43,0x40),(0xFF,0x00),(0x34,0x03),(0x35,0x44),(0xFF,0x01),
            (0x31,0x04),(0x4B,0x09),(0x4C,0x05),(0x4D,0x04),(0xFF,0x00),
            (0x44,0x00),(0x45,0x20),(0x47,0x08),(0x48,0x28),(0x67,0x00),
            (0x70,0x04),(0x71,0x01),(0x72,0xFE),(0x76,0x00),(0x77,0x00),
            (0xFF,0x01),(0x0D,0x01),(0xFF,0x00),(0x80,0x01),(0x01,0xF8),
            (0xFF,0x01),(0x8E,0x01),(0x00,0x01),(0xFF,0x00),(0x80,0x00),
        ]:
            self._w8(r, v)
        self._w8(0x0A, 0x04)
        self._w8(0x84, self._r8(0x84) & ~0x10)
        self._w8(0x0B, 0x01)
        self._w8(0x01, 0xE8)
        self._calibrar()
        self._w8(0x01, 0xE8)

    def _spad_info(self):
        self._w8(0x80, 0x01); self._w8(0xFF, 0x01); self._w8(0x00, 0x00)
        self._w8(0xFF, 0x06); self._w8(0x83, self._r8(0x83) | 0x04)
        self._w8(0xFF, 0x07); self._w8(0x81, 0x01); self._w8(0x80, 0x01)
        self._w8(0x94, 0x6B); self._w8(0x83, 0x00)
        t = utime.ticks_ms()
        while self._r8(0x83) == 0:
            if utime.ticks_diff(utime.ticks_ms(), t) > _IO_TIMEOUT:
                raise Exception("Timeout SPAD")
        self._w8(0x83, 0x01)
        tmp = self._r8(0x92)
        self._w8(0x81, 0x00); self._w8(0xFF, 0x06)
        self._w8(0x83, self._r8(0x83) & ~0x04)
        self._w8(0xFF, 0x01); self._w8(0x00, 0x01)
        self._w8(0xFF, 0x00); self._w8(0x80, 0x00)
        return (tmp & 0x7F), (tmp >> 7) & 1

    def _calibrar(self):
        for seq, vhv in [(0x01, 0x40), (0x02, 0x00)]:
            self._w8(0x01, seq)
            self._w8(0x30, vhv)
            self._w8(0x00, 0x01)
            t = utime.ticks_ms()
            while (self._r8(0x13) & 0x07) == 0:
                if utime.ticks_diff(utime.ticks_ms(), t) > _IO_TIMEOUT:
                    raise Exception("Timeout calibracion")
            self._w8(0x0B, 0x01)
            self._w8(0x00, 0x00)

    def ping(self):
        self._w8(0x80, 0x01); self._w8(0xFF, 0x01); self._w8(0x00, 0x00)
        self._w8(0x91, self._stop_var)
        self._w8(0x00, 0x01); self._w8(0xFF, 0x00); self._w8(0x80, 0x00)
        self._w8(0x00, 0x01)
        t = utime.ticks_ms()
        while self._r8(0x00) & 0x01:
            if utime.ticks_diff(utime.ticks_ms(), t) > _IO_TIMEOUT:
                raise Exception("Timeout medicion")
        t = utime.ticks_ms()
        while (self._r8(0x13) & 0x07) == 0:
            if utime.ticks_diff(utime.ticks_ms(), t) > _IO_TIMEOUT:
                raise Exception("Timeout resultado")
        dist = self._r16(0x14 + 10)
        self._w8(0x0B, 0x01)
        return dist

    def read(self):
        return self.ping()
